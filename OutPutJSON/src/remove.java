import org.json.JSONObject;

public class remove {

	
	public void removeString(JSONObject obj, String key) {
		obj.remove(key);
	}
	public void removeInt(JSONObject obj, String key) {
		obj.remove(key);
	}
	public void removeDouble(JSONObject obj, String key) {
		obj.remove(key);
	}
	public void removeBool(JSONObject obj, String key) {
		obj.remove(key);
	}
	public void removeObj(JSONObject obj, String key) {
		obj.remove(key);
	}
}
	

